#include "libft.h"

void	ft_putnbr(int n)
{
	int		sign;

	if (n < 0)
	{
		sign = -1;
		ft_putchar('-');
	}
	else
		sign = 1;
	if ((n / 10) != 0)
		ft_putnbr(sign * (n / 10));
	ft_putchar((sign * (n % 10)) + '0');
}
